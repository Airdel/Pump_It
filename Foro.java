private void addGame(Foro2 g){
        if(g==null)return;
        if(hash(g.getIdTema())==-1){showMessageDialog(this,"ID no valida"); return;}
        int pos = hash(g.getIdTema());
        while(G[pos]!=null)pos++;
        if(pos>=G.length-1) {showMessageDialog(this,"ID error"); return;}
        G[pos]=g;
    }//addToHash
