        private int buscar(String id){//retorna la posicion, si no existe: retorna -1
        if(hash(id)==-1)return -1;
        int pos = hash(id);
        while(G[pos]!=null && !G[pos].getIdTema().equals(id)) {
            pos++;
            if(pos>=G.length-1){break;}
        }
        if(G[pos]==null) return -1;
        if(!G[pos].getIdTema().equals(id))return -1;
        return pos;
    }//buscar
    

    
