     public void hashToTable(){
        m.setRowCount(0);
        Object O[] = new Object[4];
        for (int i = 0; i < G.length; i++) {
            if(G[i]==null){
                O[0] = " ";
                O[1] = " ";
                O[2] = " ";
                O[3] = " ";
            }else{
                //System.out.println("Valores del objeto a añadir:"+Z[i].getIdTema()+"---"+Z[i].getTema()+Z[i].getUsuario()+Z[i].getComentario());
                O[0] = G[i].getIdTema();
                O[1] = G[i].getTema();
                O[2] = G[i].getUsuario();
                O[3] = G[i].getComentario();
         
            
            }
            m.addRow(O);
           // System.out.println("Valores del objeto a añadir:"+Z[i].getIdTema()+"---"+Z[i].getTema()+Z[i].getUsuario()+Z[i].getComentario());
            
        }
     }//iwi
     
      private void open(){
        G = new Foro2[1000];
        try {
            FileInputStream fi = new FileInputStream("foros.fs");
            ObjectInputStream fib = new ObjectInputStream(fi);
            while (fib.read() == -1) {
                Foro2 g = (Foro2) fib.readObject();
                addGame(g);
            }
            hashToTable();
        } catch (FileNotFoundException ex) {
        } catch (IOException ex) {
        } catch (ClassNotFoundException ex) {
            System.out.println(ex.getMessage());
        }
    }//open
     
     
